let raio, altura, volume; 

raio=parseFloat(prompt("Informe o valor do raio da circuferência da lata"))
altura=parseFloat(prompt("Informe o valor do altura da lata"))

volume = 3,14159 *(raio*raio)* altura;

alert("O volume da lata é: " + volume + "m³")


